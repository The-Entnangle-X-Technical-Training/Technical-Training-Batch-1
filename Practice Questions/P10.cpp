// Area of Rectangle
//(V)- length,Width
//(O1)-input length,width
//(O2)- calculate area of Rectangle=length*width

#include<iostream>
using namespace std;

int main(){

    int length,width,AreaOfRectangle;

    cout<<"Length of Rectangle=";
    cin>>length;

    cout<<"Width of Rectangle=";
    cin>>width;

    AreaOfRectangle=length*width;
    cout<<"Area of Rectangle="<<AreaOfRectangle<<endl;
return 0;
}