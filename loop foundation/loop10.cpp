// print multiplication of table 5
// Write a program that prints multiplication table of 5 from 5×1 to 5×10.


#include <iostream>
using namespace std;
int main() {

    int i = 1;
    

    while(i<=10){
        cout <<5<<"X"<<i<<"="<<5*i<<endl;
            i++;
        }
        
    
    return 0;
}   