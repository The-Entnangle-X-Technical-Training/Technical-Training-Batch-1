// Problem 10: Print Multiplication Table of 5
// Write a program that prints multiplication table of 5 from 5×1 to 5×10

#include <iostream>
using namespace std;

int main(){
   
    for(int i = 1 ; i <=10 ; i++){
        cout << " 5 * "<< i << " = "<<5*i <<endl;
    }
    return 0;
}
